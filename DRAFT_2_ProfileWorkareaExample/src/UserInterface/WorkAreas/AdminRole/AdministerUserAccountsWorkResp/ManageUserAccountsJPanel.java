    private void UserAccountTableMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UserAccountTableMousePressed
        // Extracts the row (uaser account) in the table that is selected by the user
        int size = UserAccountTable.getRowCount();
        int selectedrow = UserAccountTable.getSelectionModel().getLeadSelectionIndex();

        if (selectedrow < 0 || selectedrow > size - 1) {
            return;
        }
        
        selecteduseraccount = (UserAccount) UserAccountTable.getValueAt(selectedrow, 0);
        if (selecteduseraccount == null) {
            return;
        
        
        }    
    }//GEN-LAST:event_UserAccountTableMousePressed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

    // Check if a user account is selected
        if (selecteduseraccount == null) {
            JOptionPane.showMessageDialog(this, "Please select an account first.", 
                                    "No Selection", JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // Simple update options - change password
    String newPassword = JOptionPane.showInputDialog(this, 
        "Enter new password for " + selecteduseraccount.getUserLoginName() + ":");
    
    if (newPassword != null && !newPassword.trim().isEmpty()) {
        selecteduseraccount.setPassword(newPassword.trim());
        selecteduseraccount.updateLastUpdatedTime(); // Update the timestamp
        
        JOptionPane.showMessageDialog(this, "Password updated successfully!", 
                                    "Success", JOptionPane.INFORMATION_MESSAGE);
        
        refreshTable(); // Refresh the table to show updated timestamp
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
    // Check if a user account is selected
    if (selecteduseraccount == null) {
        JOptionPane.showMessageDialog(this, "Please select an account first.", 
                                    "No Selection", JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // Confirm deletion
    int result = JOptionPane.showConfirmDialog(this, 
        "Are you sure you want to delete account: " + selecteduseraccount.getUserLoginName() + "?", 
        "Confirm Delete", 
        JOptionPane.YES_NO_OPTION,
        JOptionPane.WARNING_MESSAGE);
    
    if (result == JOptionPane.YES_OPTION) {
        // Remove from user account directory
        business.getUserAccountDirectory().getUserAccountList().remove(selecteduseraccount);
        
        JOptionPane.showMessageDialog(this, 
            "Account '" + selecteduseraccount.getUserLoginName() + "' deleted successfully!", 
            "Success", JOptionPane.INFORMATION_MESSAGE);
        
        selecteduseraccount = null; // Clear selection
        refreshTable(); // Refresh the table
    }    }//GEN-LAST:event_btnDeleteActionPerformed
    
    

