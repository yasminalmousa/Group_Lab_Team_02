    public void refreshTable() {
        //clear supplier table
        int rc = UserAccountTable.getRowCount();
        int i;
        for (i = rc - 1; i >= 0; i--) {
            ((DefaultTableModel) UserAccountTable.getModel()).removeRow(i);
        }

        UserAccountDirectory uad = business.getUserAccountDirectory();

        for (UserAccount ua : uad.getUserAccountList()) {
            Object[] row = new Object[5];
            row[0] = ua; // Keep the UserAccount object in the first column for selection
            row[1] = ua.getUserLoginName(); // Display username in second column
            row[2] = ua.getStatus(); // Status
            row[3] = ua.getLastLoginString(); // Last login
            row[4] = ua.getLastUpdatedString(); // Last updated

            ((DefaultTableModel) UserAccountTable.getModel()).addRow(row);
        }
    }

    
