    private void populateTranscriptData() {
        if (student != null) {
            jLabel3.setText(student.getName() != null ? student.getName() : "N/A");
            jLabel5.setText(student.getMajor() != null ? student.getMajor() : "N/A");
            jLabel8.setText(student.getStudentId() != null ? student.getStudentId() : "N/A");
            jLabel9.setText(String.format("%.2f", student.getGPA()));
            
            populateTranscriptTable();
            
            int totalCredits = calculateTotalCredits();
            jLabel11.setText(String.valueOf(totalCredits));
            jLabel13.setText(String.format("%.2f", student.getGPA()));
        }
    }
    

    private void populateTranscriptTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing data
        
        if (student != null && student.getCoursesEnrolled() != null) {
            Object[][] transcriptData = {
                {"Fall 2023", "Introduction to Programming", "CS101", "3", "A", "12.0"},
                {"Fall 2023", "Calculus I", "MATH201", "4", "A-", "14.8"},
                {"Fall 2023", "English Composition", "ENG101", "3", "B+", "9.9"},
                {"Spring 2024", "Data Structures", "CS102", "4", "B+", "13.2"},
                {"Spring 2024", "Physics I", "PHYS101", "4", "B", "12.0"},
                {"Spring 2024", "World History", "HIST101", "3", "A", "12.0"},
                {"Fall 2024", "Database Systems", "CS301", "3", "A", "12.0"},
                {"Fall 2024", "Linear Algebra", "MATH301", "3", "A-", "11.1"},
                {"Fall 2024", "Introduction to Psychology", "PSYC101", "3", "A", "12.0"}
            };
            
            for (Object[] row : transcriptData) {
                model.addRow(row);
            }
        }
    }
    

    private int calculateTotalCredits() {
        if (student == null || student.getCoursesEnrolled().isEmpty()) {
            return 30; // Sample total for demo
        }
        
        int totalCredits = 0;
        for (String courseCode : student.getCoursesEnrolled()) {
            if (courseCode.startsWith("MATH") || courseCode.startsWith("PHYS")) {
                totalCredits += 4;
            } else {
                totalCredits += 3;
            }
        }
        return totalCredits;
    }

